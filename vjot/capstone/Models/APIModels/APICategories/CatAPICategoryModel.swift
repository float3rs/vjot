import Foundation
import SwiftUI

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------

// MARK: UNFOLD (CATS CATEGORIES)

struct CatAPICategory {
    
    var id: Int // required
    var name: String // required
}

// ----------------------------------------------------------------------------

extension CatAPICategory: Codable & Identifiable {}

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------

func unfoldCatAPICategory(foldedCatAPICategory: FoldedCatAPICategory) -> CatAPICategory {
    
    let id = foldedCatAPICategory.id
    let name = foldedCatAPICategory.name
    
    // ------------------------------------------------------------------------
    
    return CatAPICategory(
        id: id,
        name: name
    )
}

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------

func unfoldCatAPICategories(foldedCatAPICategories: [FoldedCatAPICategory]) -> [CatAPICategory] {
    var catAPICategories: [CatAPICategory] = []
    
    foldedCatAPICategories.forEach { foldedCatAPICategory in
        catAPICategories.append(unfoldCatAPICategory(foldedCatAPICategory: foldedCatAPICategory))
    }
    
    return catAPICategories
}

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------

// MARK: CAT CATEGORIES DICTIONARY [String:Int]

func generateCatAPICategoriesDict(catAPICategories: [CatAPICategory]) -> [String:Int] {
    var catAPICategoriesDict: [String:Int] = [:]
    
    catAPICategories.forEach {catAPICategory in
        catAPICategoriesDict[catAPICategory.name] = catAPICategory.id
    }
    
    return catAPICategoriesDict
}

