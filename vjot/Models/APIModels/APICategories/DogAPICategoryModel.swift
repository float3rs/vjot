import Foundation
import SwiftUI

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------


// MARK: UNFOLD (DOGS CATEGORIES)

struct DogAPICategory {
    
    var id: Int // required
    var name: String // required
}

// ----------------------------------------------------------------------------

extension DogAPICategory: Codable & Identifiable {}

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------

func unfoldDogAPICategory(foldedDogAPICategory: FoldedDogAPICategory) -> DogAPICategory {
    
    let id = foldedDogAPICategory.id
    let name = foldedDogAPICategory.name
    
    // ------------------------------------------------------------------------
    
    return DogAPICategory(
        id: id,
        name: name
    )
}

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------

func unfoldDogAPICategories(foldedDogAPICategories: [FoldedDogAPICategory]) -> [DogAPICategory] {
    var dogAPICategories: [DogAPICategory] = []
    
    foldedDogAPICategories.forEach { foldedDogAPICategory in
        dogAPICategories.append(unfoldDogAPICategory(foldedDogAPICategory: foldedDogAPICategory))
    }
    
    return dogAPICategories
}

// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------
// ----------------------------------------------------------------------------
// ////////////////////////////////////////////////////////////////////////////
// ----------------------------------------------------------------------------

// MARK: DOG CATEGORIES DICTIONARY [String:Int]

func generateDogAPICategoriesDict(dogAPICategories: [DogAPICategory]) -> [String:Int] {
    var dogAPICategoriesDict: [String:Int] = [:]
    
    dogAPICategories.forEach { dogAPICategory in
        dogAPICategoriesDict[dogAPICategory.name] = dogAPICategory.id
    }
    
    return dogAPICategoriesDict
}

